def main():
    """Main function to create the fixed gold cards JSON."""
    
    # Load existing gold cards
    with open(GOLD_CARDS_PATH) as f:
        existing_cards = json.load(f)
    
    # Define which games need full replacement vs keeping
    GAMES_TO_REPLACE = {
        "REALITY_CHECK",
        "OVER_UNDER", 
        "TEXT_THREAD_TRAP",
        "POISON_PITCH",
        "HOT_SEAT_IMPOSTER",
    }
    
    # Filter out games we're replacing
    cards_to_keep = [c for c in existing_cards if c.get("game") not in GAMES_TO_REPLACE]
    
    # Combine with new cards
    new_cards = (
        REALITY_CHECK_CARDS +
        OVER_UNDER_CARDS +
        TEXT_THREAD_TRAP_CARDS +
        POISON_PITCH_CARDS +
        HOT_SEAT_IMPOSTER_CARDS
    )
    
    all_cards = cards_to_keep + new_cards
    
    # Sort by game name for organization
    all_cards.sort(key=lambda c: (c.get("game", ""), c.get("id", "")))
    
    # Verify counts
    game_counts = {}
    for card in all_cards:
        game = card.get("game", "UNKNOWN")
        game_counts[game] = game_counts.get(game, 0) + 1
    
    print("Gold Cards Distribution:")
    print("-" * 40)
    for game, count in sorted(game_counts.items()):
        status = "âœ…" if count >= 50 else "âš ï¸"
        print(f"  {status} {game}: {count} cards")
    print("-" * 40)
    print(f"Total: {len(all_cards)} cards")
    
    # Write output
    output_path = GOLD_CARDS_PATH
    with open(output_path, 'w') as f:
        json.dump(all_cards, f, indent=2)
    
    print(f"\nâœ… Fixed gold cards written to: {output_path}")
    
    # Also write a backup
    backup_path = GOLD_CARDS_PATH.with_suffix('.json.backup')
    import shutil
    if not backup_path.exists():
        shutil.copy(GOLD_CARDS_PATH, backup_path)
        print(f"ðŸ“¦ Backup created at: {backup_path}")

